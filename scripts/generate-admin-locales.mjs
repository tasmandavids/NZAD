/** Deep-walk and replace leaf strings using a map keyed by dot path */
function translateTree(node, map, prefix = "admin") {
  if (typeof node === "string") {
    return map[prefix] ?? node;
  }
  const out = {};
  for (const [k, v] of Object.entries(node)) {
    out[k] = translateTree(v, map, `${prefix}.${k}`);
  }
  return out;
}

