// For keys not in frMap, fall back to English then we'll fill remaining via auto-copy with a second pass
function buildLocale(map, locale) {
  const walk = (node, prefix = "admin") => {
    if (typeof node === "string") return map[prefix] ?? node;
    const out = {};
    for (const [k, v] of Object.entries(node)) out[k] = walk(v, `${prefix}.${k}`);
    return out;
  };
  return { admin: walk(en.admin) };
}

// Load full translation files from sibling JSON if present, else use partial map + EN fallback
for (const locale of ["fr", "it", "ru"]) {
  const fullPath = path.join(root, `scripts/admin-${locale}.json`);
  let data;
  if (fs.existsSync(fullPath)) {
    data = JSON.parse(fs.readFileSync(fullPath, "utf8"));
  } else if (locale === "fr") {
    data = buildLocale(frMap);
  } else {
    data = en; // placeholder until it/ru files written
  }
  fs.writeFileSync(path.join(root, `messages/${locale}/admin.json`), JSON.stringify(data, null, 2));
  console.log(`Wrote messages/${locale}/admin.json`);
}
